function FieldGroup({ className, ...props }: React.ComponentProps<"div">) {
  return (
    <div
      data-slot="field-group"
      className={cn(
        "group/field-group @container/field-group flex w-full flex-col gap-6 data-[slot=checkbox-group]:gap-4 *:data-[slot=field-group]:gap-5",
        className
      )}
      {...props}
    />
  )
}

const fieldVariants = cva(
  "group/field flex w-full gap-2 data-[invalid=true]:text-[color:var(--color-danger)]",
  {
    variants: {
      orientation: {
        vertical: "flex-col items-stretch *:w-full [&>.sr-only]:w-auto",
        horizontal:
          "flex-row items-start gap-4 *:data-[slot=field-label]:w-44 *:data-[slot=field-content]:flex-1 has-[>[data-slot=field-content]]:[&>[role=checkbox],[role=radio]]:mt-px",
        responsive:
          "flex-col gap-2 *:w-full [&>.sr-only]:w-auto @md/field-group:flex-row @md/field-group:items-start @md/field-group:gap-4 @md/field-group:*:data-[slot=field-label]:w-44 @md/field-group:*:data-[slot=field-content]:flex-1",
      },
    },
    defaultVariants: {
      orientation: "vertical",
    },
  }
)

function Field({
  className,
  orientation = "vertical",
  ...props
}: React.ComponentProps<"div"> & VariantProps<typeof fieldVariants>) {
  return (
    <div
      role="group"
      data-slot="field"
      data-orientation={orientation}
      className={cn(fieldVariants({ orientation }), className)}
      {...props}
    />
  )
}

